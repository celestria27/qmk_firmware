bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        /* KEYBOARD SPACESHIP STATUS START */
        case KC_W:
        case KC_K:
        case KC_UP:
            if (record->event.pressed) {
                if(y_spaceship > MIN_SPACESHIP_DIM) {
                    y_spaceship--;
                }
            }
            break;
        case KC_S:
        case KC_J:
        case KC_DOWN:
            if (record->event.pressed) {
                if(y_spaceship < MAX_SPACESHIP_DIM) {
                    y_spaceship++;
                }
            }
            break;
        case KC_A:
        case KC_H:
        case KC_LEFT:
            if (record->event.pressed) {
                if(x_spaceship > LEFT_SPACESHIP_DIM) {
                    x_spaceship--;
                }
            }
            break;
        case KC_D:
        case KC_L:
        case KC_RGHT:
            if (record->event.pressed) {
                if(x_spaceship < RIGHT_SPACESHIP_DIM) {
                    x_spaceship++;
                }
            }
            break;

        /* Fire when pressing space*/
        case KC_SPC:
            if (record->event.pressed) {
                for (int i = 0; i < MAX_BULLETS; i++) {
                    if(bullets[i] == -1) {
                        bullets[i] = y_spaceship - 1;
                        break;
                    }
                }
            }
            break;
        /* KEYBOARD SPACESHIP STATUS END */
    }
    return true;
}
