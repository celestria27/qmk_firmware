static void print_bars(void) {

    if (timer_elapsed32(anim_timer) > BARS_ANIM_FRAME) {
        anim_timer = timer_read32();
        const char* frame = current_wpm > FAST_TYPE_WPM ? FAST_TYPE_FRAMES[bar_current_frame] : SLOW_TYPE_FRAMES[bar_current_frame];
        oled_write_raw_P(frame, BARS_ANIM_SIZE);
        bar_current_frame = (bar_current_frame + 1) % 3;

        sprintf(wpm_str, "%03d", get_current_wpm());
        oled_set_cursor(0, 1);
        oled_write_P(PSTR("WPM: "), false);
        oled_set_cursor(0, 2);
        oled_write(wpm_str, false);
    }

}

